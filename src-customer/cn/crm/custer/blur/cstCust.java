package cn.crm.custer.blur;

public class cstCust {
	
	private Long mana;//客户经理编
	private Long rankid;//等级编号
	private String disc;//地区名字
	private String client;//客户名称
	
	
	
	
	public Long getMana() {
		return mana;
	}
	public void setMana(Long mana) {
		this.mana = mana;
	}
	public Long getRankid() {
		return rankid;
	}
	public void setRankid(Long rankid) {
		this.rankid = rankid;
	}
	public String getDisc() {
		return disc;
	}
	public void setDisc(String disc) {
		this.disc = disc;
	}
	public String getClient() {
		return client;
	}
	public void setClient(String client) {
		this.client = client;
	}
	
	

}
